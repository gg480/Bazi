/**
 * 六十四命格类型系统
 *
 * 基于《周易》六十四卦，以六爻测定法推演人格命格。
 * 六道天问，每问阴阳二选一，六爻成卦，自然映射六十四卦。
 * 每卦对应一个四字命格名，取义于传统典籍、诗词典故、神话传说。
 *
 * 编码规则：
 * - bit0 = 初爻（第一题·问心）
 * - bit1 = 二爻（第二题·问性）
 * - bit2 = 三爻（第三题·问行）
 * - bit3 = 四爻（第四题·问势）
 * - bit4 = 五爻（第五题·问志）
 * - bit5 = 上爻（第六题·问命）
 * - 1 = 阳爻（甲选），0 = 阴爻（乙选）
 */

