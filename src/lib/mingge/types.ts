/** 根据ID获取命格类型 */
export function getMingGeById(id: number): MingGeType | undefined {
  return MING_GE_64.find(t => t.id === id);
}

/** 根据六爻答案计算命格ID */
export function calculateMingGeId(answers: YaoValue[]): number {
  // answers[0] = 初爻(bit0), answers[5] = 上爻(bit5)
  let id = 0;
  for (let i = 0; i < 6; i++) {
    if (answers[i]) {
      id |= (1 << i);
    }
  }
  return id;
}

